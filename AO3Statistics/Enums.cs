namespace AO3Statistics;

/// <summary>
/// A list of available statistic types.
/// </summary>
public enum StatTypes
{
    /// <summary>
    /// Number of hits on the fic.
    /// </summary>
    Hits,

    /// <summary>
    /// Number of kudos on the fic.
    /// </summary>
    Kudos,
    /// <summary>
    /// Word count of published chapters.
    /// </summary>
    Words,
    /// <summary>
    /// Number of published chapters.
    /// </summary>
    Chapters,
}